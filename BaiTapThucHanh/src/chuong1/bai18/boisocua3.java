public class boisocua3 {
	public static void main(String[] args) {
        System.out.println("Các bội số của 3 từ 300 đến 3 là:");
        for (int i = 300; i >= 3; i--) {
            if (i % 3 == 0) {
                System.out.print(i + " ");
            }
        }
        System.out.println(); 
    }

}
