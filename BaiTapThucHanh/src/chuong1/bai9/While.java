package chuong1.bai9;

/**
 * 
 */
public class While {
	    public static void main(String[] args) {
	        int count = 1; // Biến đếm bắt đầu từ 1

	        // Lặp khi count < 11
	        while (count < 11) {
	            System.out.println("Count is: " + count);
	            count++; // Tăng biến đếm
	        }
	    }

	    // Vòng lặp while kiểm tra điều kiện trước khi thực hiện lặp.
	}



