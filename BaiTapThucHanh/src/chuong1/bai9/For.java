public class For {
	    public static void main(String[] args) {
	        // Vòng lặp for từ 1 đến 10
	        for (int i = 1; i < 11; i++) {
	            System.out.println("Count is: " + i);
	        }
	    }

	    // Dùng vòng lặp for để in ra các số từ 1 đến 10.
	}



